// Add Two numbers

import java.util.Scanner;

/**
 * AddTwoNum
 */
public class AddTwoNum {

    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        int a = s.nextInt();
        int b = s.nextInt();

        int sum;
        sum = a + b;
        
        System.out.println(sum);
    }
}